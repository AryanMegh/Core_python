'''
Q42) Write a program to calculate average of three numbers?
'''

#Taking 3 number from user.

Num1 = int( input( " Enter first number: " ) )
Num2 = int( input( " Enter second number: " ) )
Num3 = int( input( " Enter third number: " ) )

#Formula to calculate average from three numbers.
Avg = (Num1 + Num2 + Num3)/3

#Print function to print perfect average from three number.

print( " The average from three number is: ", Avg )